#include <stdio.h>

int main() {
    double t1, t2, t3; // оголошення змінних для зберігання часу, за який кожен гість може з'їсти пиріг
    scanf("%lf %lf %lf", &t1, &t2, &t3); // зчитування значень t1, t2 та t3 зі стандартного вводу
    double result = 1 / (1/t1 + 1/t2 + 1/t3); // обчислення часу, за який гості разом можуть з'їсти пиріг
    printf("%.2lf\n", result); // виведення результату з точністю до двох знаків після коми
    return 0;
}
